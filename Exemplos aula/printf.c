#include <stdio.h>

void main() {
    int a = 12;
    float b = 7.23;
    char c = 'f';
    unsigned int d = 123;
    unsigned long int e = 999999999999999;
    double f = 212121221;
    char texto[12] = "Hello Word";

    // int
    printf("a=%d\n", a);

    // float
    printf("b=%f\n", b);

    // char
    printf("c=%c\n", c);

    // unsigned int
    printf("d=%u\n", d);

    // unsigned long int
    printf("e=%lu\n", e);

    // double
    printf("f=%g\n", f);

    // texto
    printf("%s\n", texto);
}